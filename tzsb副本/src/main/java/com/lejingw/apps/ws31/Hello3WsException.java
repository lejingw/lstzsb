package com.lejingw.apps.ws31;

public class Hello3WsException extends Exception {

	public Hello3WsException() {
		super();
	}

	public Hello3WsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public Hello3WsException(String arg0) {
		super(arg0);
	}

	public Hello3WsException(Throwable arg0) {
		super(arg0);
	}

}
